extern uint32_t volatile const SysTick_ReloadVal;
extern uint32_t volatile const SysTick_ClockSource;
extern uint32_t volatile const SysTick_Interrupt;
/**********************************************************************************************************************
 *  LOCAL FUNCTION PROTOTYPES
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  LOCAL FUNCTIONS
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL FUNCTIONS
 *********************************************************************************************************************/
void SYSTEMTIMER_Init(void)
{
  /*Disabling the Systick peripheral*/
  SysTick->CTRL &= ~SysTick_CTRL_ENABLE_Msk;
  /*Setting the Reload Value*/
  SysTick->LOAD = SysTick_ReloadVal;
  /*Setting the Counter value to zero and clearing the Flag*/
  SysTick->VAL = 0;
  /*disabling systick Interrupt*/
  SysTick->CTRL &= ~SysTick_CTRL_TICKINT_Msk;
  SysTick->CTRL |= (SysTick_Interrupt<<SysTick_CTRL_TICKINT_Pos);
  /*selecting the clock source*/
  SysTick->CTRL &= ~SysTick_CTRL_CLKSOURCE_Msk;
  SysTick->CTRL |= (SysTick_ClockSource<<SysTick_CTRL_CLKSOURCE_Pos);
  /*Enable the Systick and */
  SysTick->CTRL |= SysTick_CTRL_ENABLE_Msk;
}

